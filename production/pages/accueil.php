<h2>Summary:</h2>
<p>This project aims at realising:<br>
• a database to store job advertisements,<br>
• a webpage (front end) using Javascript technologies to display an online job advertisements board as well as an adminitration page for the admin user,<br>
• an API (back end)<br>
• to allow user to apply to jobs<br>
• to manage the DB (only for admin user) - CRUD operations (Create, Read, Update, Delete)</p>
</article>


<h2>STEP 01:</h2>
<p>
Create an SQL database to store job advertisements.<br>
It must contain at least:<br>
• a table to store advertisements,<br>
• a table to store companies,<br>
• a table to store people (whether in charge of an advertisement or applying to an advertisement),<br>
• a table to keep information about a job application (referencing the mails sent, the people concerned, the ad concerned,. . . ).
</p>
</article>



<article>
<img src="dres/needleleftred.png" width=70vw height=70vh>
<h2>STEP 02:</h2>
<p>
Write a HTML/CSS page showing several job advertisements.<br>
For each ad, there must be at least a place for a title, one for a short description, and a “learn more” button.<br>
Clicking on this “learn more” button does not have any action for the moment.
</p>
</article>

<article>
<img src="dres/needleleftyellow.png" width=70vw height=70vh>
<h2>STEP 03:</h2>
<p>
The “learn more” button must now display all the available information about the ad (full description, wages, place, working time,. . . ), without reloading the page.<br>
No popup.<br>
Keep the database fields coherent with the information you display on the HTML page.
</p>
</article>

<article>
<img src="dres/needleleftred.png" width=70vw height=70vh>
<h2>STEP 04:</h2>
<p>
Create an API providing CRUD operations on the database tables (Create, Read, Update, Delete).<br>
The “learn more” button must be linked to an API route to dynamically fetch the job infos.
</p>
</article>

<article>
<img src="dres/needleleftyellow.png" width=70vw height=70vh>
<h2>STEP 05:</h2>
<p>
On your webpage, add an “Apply” button for each ad.<br>
When this button is clicked, it opens a form to<br>
• enter information about you (name, email, phone,. . . )<br>
• send a message to the owner of the ad (you).<br>
This action must be saved in the database.
</p>
</article>

<article>
<img src="dres/needleleftred.png" width=70vw height=70vh>
<h2>STEP 06:</h2>
<p>
Add an authentication mecanism on the webpage.<br>
When identified, you don’t have to fill in your personal information to apply to a job.
</p>
</article>

<article>
<img src="dres/needleleftyellow.png" width=70vw height=70vh>
<h2>STEP 07:</h2>
<p>
Create a HTML/CSS page for monitoring the database.<br>
From this page the user can list all the records of your tables, can create new records and can update or delete the existing ones.<br>
This page can be accessed only by an admin. So a successful connection rediret to this page for an admin user or to the page created at step 02 otherwise.<br>
</p>
</article>



